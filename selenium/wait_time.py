import time
import math
from selenium import webdriver
from selenium.webdriver.common.by import By
from selenium.webdriver.support.ui import WebDriverWait
from selenium.webdriver.support import expected_conditions as EC


def calc(x):
  return str(math.log(abs(12*math.sin(int(x)))))

try:
    browser = webdriver.Chrome()
    # говорим WebDriver ждать все элементы в течение 5 секунд
    # browser.implicitly_wait(5)

    browser.get("http://suninjuly.github.io/explicit_wait2.html")

    # говорим Selenium проверять в течение 5 секунд, пока кнопка не станет кликабельной
    price = WebDriverWait(browser, 15).until(
        EC.text_to_be_present_in_element((By.ID, "price"), '$100')
    )
    btn = browser.find_element(By.ID, "book")
    btn.click()

    value = browser.find_element(By.ID, 'input_value')
    y = calc(value.text)
    
    input1 = browser.find_element(By.ID, 'answer')
    input1.send_keys(y)

    btn = browser.find_element(By.CSS_SELECTOR, '[type=submit]')
    btn.click()
    
    # assert "successful" in message.text
    time.sleep(30)
finally:
    browser.quit()



# 1. title_is
# Описание: Ожидает, что заголовок текущей страницы будет равен заданному тексту.
# Применение: Используется, когда необходимо убедиться, что заголовок страницы соответствует ожидаемому значению.
# 2. title_contains
# Описание: Ожидает, что заголовок текущей страницы будет содержать заданный текст.
# Применение: Полезно, когда заголовок может варьироваться, но вы хотите убедиться, что он содержит определённое слово или фразу.
# 3. presence_of_element_located
# Описание: Ожидает, что элемент будет присутствовать в DOM (Document Object Model) страницы, но не обязательно видим.
# Применение: Используется для проверки наличия элемента на странице, например, перед выполнением действий с ним.
# 4. visibility_of_element_located
# Описание: Ожидает, что элемент будет видим (т.е. не только присутствует в DOM, но и имеет размер больше нуля).
# Применение: Полезно, когда необходимо убедиться, что элемент доступен для взаимодействия, например, перед кликом.
# 5. visibility_of
# Описание: Ожидает, что переданный элемент будет видим (т.е. не только присутствует в DOM, но и имеет размер больше нуля).
# Применение: Аналогично visibility_of_element_located, но применяется к уже найденному элементу.
# 6. presence_of_all_elements_located
# Описание: Ожидает, что все указанные элементы будут присутствовать в DOM страницы.
# Применение: Используется, когда нужно убедиться, что несколько элементов загружены на странице.
# 7. text_to_be_present_in_element
# Описание: Ожидает, что заданный текст будет присутствовать в указанном элементе.
# Применение: Полезно для проверки, что элемент содержит ожидаемое значение текста.
# 8. text_to_be_present_in_element_value
# Описание: Ожидает, что заданный текст будет присутствовать в значении атрибута элемента (например, в поле ввода).
# Применение: Используется для проверки, что поле ввода содержит ожидаемое значение.
# 9. frame_to_be_available_and_switch_to_it
# Описание: Ожидает, что фрейм будет доступен и переключается на него.
# Применение: Полезно, когда нужно работать с элементами внутри фрейма.
# 10. invisibility_of_element_located
# Описание: Ожидает, что элемент будет невидимым (либо отсутствует в DOM, либо видим, но его размер равен нулю).
# Применение: Используется для проверки, что элемент больше не виден на странице, например, после закрытия уведомления.
# 11. element_to_be_clickable
# Описание: Ожидает, что элемент будет доступен для клика (т.е. присутствует в DOM и видим).
# Применение: Полезно перед выполнением клика на элемент, чтобы избежать ошибок.
# 12. staleness_of
# Описание: Ожидает, что элемент станет устаревшим (т.е. элемент больше не доступен в DOM).
# Применение: Используется для проверки, что элемент был удалён со страницы, например, после перезагрузки.
# 13. element_to_be_selected
# Описание: Ожидает, что элемент (например, чекбокс или радиокнопка) будет выбран.
# Применение: Полезно для проверки состояния элемента перед выполнением действий.
# 14. element_located_to_be_selected
# Описание: Ожидает, что указанный элемент будет выбран.
# Применение: Используется для проверки состояния элемента, который находится в DOM.
# 15. element_selection_state_to_be
# Описание: Ожидает, что элемент будет иметь указанное состояние выбора (выбран или не выбран).
# Применение: Полезно для проверки состояния чекбоксов или радиокнопок.
# 16. element_located_selection_state_to_be
# Описание: Ожидает, что указанный элемент будет иметь указанное состояние выбора.
# Применение: Используется для проверки состояния элемента, который находится в DOM.
# 17. alert_is_present
# Описание: Ожидает, что на странице появится всплывающее окно (alert).
# Применение: Полезно для работы с JavaScript-уведомлениями и подтверждениями.
